


// and Logic(&&)  It is used to combine two boolean expressions, and the resulting expression is true only if both of the combined expressions are true.
void main(){
bool isSunny = true;
bool isDark = false;

bool result = isSunny && isDark ; 

print('hasil dari $isSunny && $isDark adalah $result') ; // output : hasil dari true && false adalah false 



// or logic (||) operator logika mengembalikan true jika salah satu atau kedua operand adalah true
bool Jessica = true; 
bool Joko = false;

bool hasil  = Joko || Jessica  ;

print('Hasil dari $Jessica || $Joko adalah $hasil'); // output : hasil dari true || false adalah true 

//not logic // operator not mengembalikan nilai boolean dari operand 
bool a = true;
// not logic 
bool reslut = !a;
print('hasil dari !$a adalah $reslut'); //output dari !true adalah false 

}













